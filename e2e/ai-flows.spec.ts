test('AI: admin closes voting â†’ AI writes decision summary + announcement', async ({ page, request }) => {
  test.setTimeout(90_000);
  const { token } = await loginApi(request, 'admin@condoos.dev', 'admin123');
  const headers = { Authorization: `Bearer ${token}`, 'Content-Type': 'application/json' };

  // Create a fresh proposal in voting state with a couple of votes
  const created = await request.post(`${apiURL}/proposals`, {
    headers,
    data: { title: `E2E decision ${Date.now()}`, description: 'Proposta para validar o fluxo de decisĂŁo da IA.', category: 'maintenance', estimated_cost: 12000 },
  });
  const propId = (await created.json()).data.id;
  // Move to voting
  await request.post(`${apiURL}/proposals/${propId}/status`, { headers, data: { status: 'voting' } });
  // Cast a yes vote as admin
  await request.post(`${apiURL}/proposals/${propId}/vote`, { headers, data: { choice: 'yes' } });
  // Cast a yes as resident
  const resident = await loginApi(request, 'resident@condoos.dev', 'resident123');
  await request.post(`${apiURL}/proposals/${propId}/vote`, {
    headers: { Authorization: `Bearer ${resident.token}`, 'Content-Type': 'application/json' },
    data: { choice: 'yes' },
  });

  // Drive the UI: navigate as admin, click "Close & AI decision"
  await browserLogin(page, request, 'admin');
  await page.goto(`/board/proposals/${propId}`);
  // The label may be EN or PT-BR; cover both
  const closeBtn = page.getByRole('button', { name: /Close.*AI decision|Encerrar.*decisĂŁo|AI decision/i }).first();
  await expect(closeBtn).toBeVisible({ timeout: 10_000 });
  await closeBtn.click();

  // Wait for the decision summary card to render â€” look for the AI-generated headline
  await expect(page.getByText(/decisĂŁo|decision|aprovada|approved|rationale|justificativa/i).first()).toBeVisible({ timeout: 60_000 });
});

