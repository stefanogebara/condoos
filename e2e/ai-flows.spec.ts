test('AI: admin generates assembly agenda via AI', async ({ page, request }) => {
  test.setTimeout(75_000);
  const { token } = await loginApi(request, 'admin@condoos.dev', 'admin123');
  const headers = { Authorization: `Bearer ${token}`, 'Content-Type': 'application/json' };

  // Create empty draft assembly
  const future = new Date(Date.now() + 7 * 24 * 3600 * 1000).toISOString();
  const created = await request.post(`${apiURL}/assemblies`, {
    headers, data: { title: `E2E AI agenda ${Date.now()}`, kind: 'ordinary', first_call_at: future },
  });
  const assemblyId = (await created.json()).data.id;

  await browserLogin(page, request, 'admin');
  await page.goto(`/board/assemblies/${assemblyId}`);

  // The "Draft with AI" CTA only renders when the agenda is empty
  await page.getByRole('button', { name: /Draft with AI|Redigir com IA|Sugerir pauta/i }).first().click();

  // Wait for at least one agenda item to appear (AI returns 4-6 typically).
  // Items render as a numbered list with `#1`, `#2`… order pills.
  await expect(page.locator('span.font-mono').filter({ hasText: /^#1$/ }).first()).toBeVisible({ timeout: 45_000 });

  // Verify via API that items got persisted
  const detail = await request.get(`${apiURL}/assemblies/${assemblyId}`, { headers });
  const body = (await detail.json()).data;
  expect(body.agenda.length, 'expected ≥1 agenda item from AI draft').toBeGreaterThanOrEqual(1);
});
