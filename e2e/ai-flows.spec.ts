test('AI: admin pastes meeting notes → AI summarizes', async ({ page, request }) => {
  test.setTimeout(90_000);
  // Create a meeting via API so the test is deterministic
  const { token } = await loginApi(request, 'admin@condoos.dev', 'admin123');
  const headers = { Authorization: `Bearer ${token}`, 'Content-Type': 'application/json' };
  const future = new Date(Date.now() + 24 * 3600 * 1000).toISOString();
  const created = await request.post(`${apiURL}/meetings`, {
    headers,
    data: { title: `E2E AI summarize ${Date.now()}`, scheduled_for: future, agenda: 'Testar resumo da IA' },
  });
  expect(created.ok()).toBeTruthy();
  const meetingId = (await created.json()).data.id;

  await browserLogin(page, request, 'admin');
  await page.goto(`/board/meetings/${meetingId}`);

  // Paste raw notes into the textarea
  const notes = `
- Síndico abriu a reunião 20:05.
- Aprovaram a troca do ar do saguão por 4 a 1, contratada Cool Breeze HVAC, R$ 47.000.
- Discutiram carregadores EV — vai esperar segundo orçamento antes de votar.
- Reclamação da esteira #3 da academia — chamado aberto com Fitness Pro.
- Reunião encerrada 21:10.
  `.trim();
  await page.locator('textarea').first().fill(notes);

  // Click Summarize
  await page.getByRole('button', { name: /Summarize|Resumir com IA|Summarize with AI/i }).first().click();

  // Wait for the AI summary card to render (typically takes 5-15s for Claude Haiku)
  // Look for a heading or text indicating the summary succeeded
  await expect(page.getByText(/decisão|decision|summary|resumo|recap/i).first()).toBeVisible({ timeout: 60_000 });
});

