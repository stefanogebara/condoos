// 1. Resident → Suggest → AI draft → Promote to proposal
// ---------------------------------------------------------------------------

test('AI: resident suggest → AI drafts a proposal → promote', async ({ page, request }) => {
  test.setTimeout(60_000);
  await browserLogin(page, request, 'resident');
  await page.goto('/app/suggest');

  // Type a free-text complaint. Prefix with "E2E " so the cleanup script can
  // match by body LIKE 'E2E %' instead of an exact-sentence pattern that
  // silently leaks rows the moment anyone edits the test copy.
  await page.locator('textarea').fill('E2E A iluminação do hall do 3º andar fica piscando à noite, parece que vai apagar a qualquer momento.');

  // Click Submit ("Enviar")
  await page.getByRole('button', { name: /^Enviar$|^Submit$/i }).click();

  // Wait for the AI draft to appear (badge "Proposta redigida pela IA" or "AI-drafted")
  await expect(page.getByText(/Proposta redigida pela IA|AI-drafted proposal/i)).toBeVisible({ timeout: 45_000 });
  // Title generated by AI must be non-empty + reasonable length
  const titleEl = page.locator('h3.font-display').first();
  await expect(titleEl).toBeVisible();
  const title = (await titleEl.textContent() || '').trim();
  expect(title.length, `expected non-empty AI title; got "${title}"`).toBeGreaterThan(5);

  // Promote to proposal button visible (Enviar ao síndico / Send to the board)
  await expect(page.getByRole('button', { name: /Enviar ao síndico|Send to the board/i })).toBeVisible();
});

